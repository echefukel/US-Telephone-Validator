


const telephoneCheck = (str)=> {
    const allRegex = [
  // 1 555 555 5555
  /^1 \d{3} \d{3} \d{4}$/g,
  // 5555555555
  /^\d{10}$/,
  // 555 555 5555
  /^\d{3} \d{3} \d{4}$/,
  // (555) 555-5555
  /^\(\d{3}\) \d{3}-\d{4}$/,
  // (555)555-5555
  /^\(\d{3}\)\d{3}-\d{4}$/,
  // 555-555-5555
  /^\d{3}-\d{3}-\d{4}$/,
  //1 555-555-5555
  /^1 \d{3}-\d{3}-\d{4}$/,
  // 1 (555) 555-5555
  /^1 \(\d{3}\) \d{3}-\d{4}$/,
  // 1(555)555-5555
  /^1\(\d{3}\)\d{3}-\d{4}$/
  
  
  
  
  
  
  
    ]
    return allRegex.some((each) => each.test(str))
  }
  
